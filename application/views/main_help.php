<div>
	<h1>Breakthrough Foundry Help</h1>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">General Overview</h3>
	  </div>
	  <div class="panel-body">
	    <p>The goal of this web application is to provide a functional, interesting and fun environment for creative collaboration. We are working to create a social network and project management space that is stripped down to its most essential features - one that allows creative individuals to find each other, communicate and self-organize to work on collaborative projects.</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Create An Account</h3>
	  </div>
	  <div class="panel-body">
	    <p>It costs nothing to join the Breakthrough Foundry and run your first project. Simply click on “Create an Account” on the login screen. You can join at any time, or if a project administrator invites you to join a project (you’ll then receive an email with full instructions).</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Your User Profile</h3>
	  </div>
	  <div class="panel-body">
	    <p>In the Breakthrough Foundry, your user profile consists of one short text description, your location plus a set of Skill and Interest tags. You can have up to ten of each. You can view your profile by selecting the “My Profile” menu item under your name on the right side of the menu bar. You can also view the profiles of any collaborators by clicking on their name in a project’s Team List page.</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Editing Your Profile</h3>
	  </div>
	  <div class="panel-body">
	    <p>You can edit your profile information by clicking the “My Settings” menu item  under your name on the right side of the menu bar. Here you can change your account information and basic profile info. You can also add and delete tags and upload a profile image.</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Creating a Project</h3>
	  </div>
	  <div class="panel-body">
	    <p>To create a project, click the Create New Project button on your project list page. You can enter basic project information here. If you create a project, you will then be the administrator. You can edit the project, delete the project and manage the project team. Once a project is created, it will appear on your Projects page. Simply click on the project name to access all aspects of your project.</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Adding Collaborators</h3>
	  </div>
	  <div class="panel-body">
	    <p>From a project page, you can click on the Team Members button to see the team members already in your project. If you are an administrator of the project, you can add team members by clicking the Invite New Member button below the team list. Simply enter their email address and the Breakthrough Foundry will send them an email with an access key to join the project.</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Project Schedule</h3>
	  </div>
	  <div class="panel-body">
	    <p>Your project can also keep track of vital dates. The Project Schedule page will show you what’s coming up for your project and what important events have occurred in the past. Click Create Event on the Project Schedule Page to make a new event for your project. The Project Schedule page will also show you any tasks that you have assigned to you that have a specified completion date.</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Project Communication</h3>
	  </div>
	  <div class="panel-body">
	    <p>The Project Chat page allows you to communicate with your project team in real time. When a project is created, it will automatically create the #general channel for general project communication. When you are on the Project Chat page, a new menu will appear at the top of the screen. This will let you change topic channels, create a new channel or enter into a private chat with a specific team member.</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Project Tasks</h3>
	  </div>
	  <div class="panel-body">
	    <p>The heart of a project is the tasks needed to see it to completion. You can create as many tasks as you need to for your projects, and associate them into Task Groups for easier organization. Simply click the Create New Task button on the Project Task page to create a task. All projects have a special Task Group called “Archive” which let’s you set aside completed or unneeded tasks.</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Project Documents</h3>
	  </div>
	  <div class="panel-body">
	    <p>Each project in the Breakthrough Foundry has a space for project documents that are shared by the project team. Simply select a file and click Upload to put a file in the project archive. Click on a file name to view or save a document.</p>
	  </div>
	</div>
	<div class="panel panel-primary">
	  <div class="panel-heading">
	    <h3 class="panel-title">Tracking Contributions</h3>
	  </div>
	  <div class="panel-body">
	    <p>In some projects, you might want to track team member contributions of time, cash or other resources. Simply access the Contribution Tracker to log any contributions. These may be associated with a specific task or they might stand on their own. Only project administrators can approve a project contribution.</p>
	  </div>
	</div>
</div>
